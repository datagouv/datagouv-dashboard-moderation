<template>
  <div class="user_update">

    <NavCrumbs
      :crumbs="crumbs"
    />

    <div>
      <b-sidebar
        id="sidebar-moderation"
        title="Moderation"
        width="600px"
        bg-variant="light"
        text-variant="dark"
        shadow
        backdrop
        >
        <div class="px-3 py-2">
          <ModerationRowCard
            :hasHeader="true"
            :dgfType="dgfType"
            :endpoint="endpointModeration"
            :item="user"
          />
        </div>
      </b-sidebar>
    </div>

    <b-row class="mx-0">

      <!-- MODERATION BOX -->
      <!-- <b-col sm="6" md="4">
        <ModerationRowCard
          :hasHeader="true"
          :dgfType="dgfType"
          :endpoint="endpointModeration"
          :item="user"
        />
      </b-col> -->

      <!-- DISPLAY USER -->
      <b-col class="px-0">
        <UserCard
          :cardTitle="`${$t('basics.user')} nÂ° ${userId}`"
          :cardFooter="undefined"
          :userData="user"
          :userId="userId"
          height="800px"
        >
        </UserCard>
      </b-col>

    </b-row>

  </div>
</template>

<script>
import { mapState, mapGetters } from 'vuex'

import NavCrumbs from '@/components/ux/NavCrumbs.vue'
import ModerationRowCard from '@/components/moderation/ModerationRowCard.vue'

import UserCard from '@/components/users/UserCard.vue'

export default {
  name: 'UserUpdate',
  components: {
    NavCrumbs,
    ModerationRowCard,
    UserCard
  },
  data () {
    return {
      isLoading: false,
      dgfType: 'user',
      getOperationId: 'get_user',
      putOperationId: 'update_user',
      endpointModeration: 'user',
      userId: this.$route.params.id,
      userRequest: undefined,
      user: undefined,
      needsModerationData: false,
      crumbs: [
        {
          text: this.$t('home.name'),
          to: '/'
        },
        {
          text: this.$t('basics.users', { list: '' }),
          to: '/users'
        },
        {
          text: '...',
          active: true
        }
      ]
    }
  },
  created () {
    this.getUser()
  },
  watch: {
    async user (next) {
      if (next && this.needsModerationData && this.isAuthenticated) {
        this.user = await this.appendModerationData(next)
      }
    },
    '$route.params.id' (next) {
      this.userId = next
      this.getUser()
    }
  },
  computed: {
    ...mapState({
      log: (state) => state.log
    }),
    ...mapGetters({
      isAuthenticated: 'oauth/isAuthenticated'
    })
  },
  methods: {
    async appendModerationData (itemObject) {
      const itemStatus = await this.$MODERATIONcli.getModeration(this.dgfType, itemObject)
      this.$makeToast(itemStatus, this.user.id, itemStatus.method ? itemStatus.method : 'GET', this.dgfType, 'item')
      const consolidated = this.$MODERATIONcli.addModerationData(itemObject, itemStatus)
      this.needsModerationData = false
      return consolidated
    },
    getUser () {
      const API = this.$APIcli

      const params = { user: this.userId }
      this.isLoading = true
      API._request(this.getOperationId, { params }).then(
        results => {
          this.userRequest = results.url
          this.user = results.body
          this.needsModerationData = true
          this.crumbs[2].text = `${this.user.first_name} ${this.user.last_name}`
          this.isLoading = false
        },
        reason => {
          console.error(`failed on api call: ${reason}`)
          this.isLoading = false
        }
      )
    }
  }
}

</script>
